// Current language
let currentLang = localStorage.getItem('language') || 'pt';

// Function to set language
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    applyTranslations();
    updateLanguageButtons();
}

// Function to get translation
function t(key) {
    return translations[currentLang][key] || key;
}

// Function to apply translations
function applyTranslations() {
    // Navigation
    document.querySelectorAll('[data-t="nav_home"]').forEach(el => el.textContent = t('nav_home'));
    document.querySelectorAll('[data-t="nav_sofas"]').forEach(el => el.textContent = t('nav_sofas'));
    document.querySelectorAll('[data-t="nav_services"]').forEach(el => el.textContent = t('nav_services'));
    document.querySelectorAll('[data-t="nav_contact"]').forEach(el => el.textContent = t('nav_contact'));
    document.querySelectorAll('[data-t="nav_admin"]').forEach(el => el.textContent = t('nav_admin'));
    
    // Hero
    const heroTitle = document.querySelector('[data-t="hero_title"]');
    if (heroTitle) heroTitle.textContent = t('hero_title');
    const heroSubtitle = document.querySelector('[data-t="hero_subtitle"]');
    if (heroSubtitle) heroSubtitle.textContent = t('hero_subtitle');
    document.querySelectorAll('[data-t="hero_delivery"]').forEach(el => el.textContent = t('hero_delivery'));
    document.querySelectorAll('[data-t="hero_install"]').forEach(el => el.textContent = t('hero_install'));
    document.querySelectorAll('[data-t="hero_warranty"]').forEach(el => el.textContent = t('hero_warranty'));
    const heroBtn = document.querySelector('[data-t="hero_button"]');
    if (heroBtn) heroBtn.textContent = t('hero_button');
    
    // Products
    const productsTitle = document.querySelector('[data-t="products_title"]');
    if (productsTitle) productsTitle.textContent = t('products_title');
    const productsSubtitle = document.querySelector('[data-t="products_subtitle"]');
    if (productsSubtitle) productsSubtitle.textContent = t('products_subtitle');
    
    // Product buttons
    document.querySelectorAll('[data-t="order_button"]').forEach(el => el.textContent = t('order_button'));
    
    // Services
    const servicesTitle = document.querySelector('[data-t="services_title"]');
    if (servicesTitle) servicesTitle.textContent = t('services_title');
    
    // Contact
    const contactTitle = document.querySelector('[data-t="contact_title"]');
    if (contactTitle) contactTitle.textContent = t('contact_title');
    
    // Footer
    const footerRights = document.querySelector('[data-t="footer_rights"]');
    if (footerRights) footerRights.textContent = t('footer_rights');
    const footerServing = document.querySelector('[data-t="footer_serving"]');
    if (footerServing) footerServing.textContent = t('footer_serving');
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : currentLang === 'en' ? 'en' : 'pt';
}

// Update language button states
function updateLanguageButtons() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === currentLang) {
            btn.classList.add('active');
        }
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    applyTranslations();
    updateLanguageButtons();
});
